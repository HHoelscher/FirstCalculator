function sum(firstValue, secondValue) {
    return firstValue + secondValue
}

function sub(firstValue, secondValue) {
    return firstValue - secondValue
}

function mult(firstValue, secondValue) {
    return firstValue * secondValue
}

function div(firstValue, secondValue) {
    return firstValue / secondValue
}

